import { PROFESSIONAL_IMAGES } from '@/constants/images';
import { CategorySlug, Professional, ServiceCity, ServicePackage } from '@/types';

export interface ZoneProfessionalSeed {
  id: string;
  name: string;
  categorySlug: CategorySlug;
  category: string;
  city: ServiceCity;
  serviceAreas: ServiceCity[];
  rating: number;
  reviewCount: number;
  priceFrom: number;
  availableToday: boolean;
  imageKey: keyof typeof PROFESSIONAL_IMAGES;
  avatarColor: string;
  distanceKm: number;
  verified?: boolean;
}

const DEFAULT_BIO = 'Professionista verificato su Fidati, disponibile per interventi rapidi e lavori programmati.';
const DEFAULT_WHY = [
  'Recensioni verificate',
  'Prezzi trasparenti',
  'Assicurazione RC professionale',
  'Assistenza post-intervento',
];

function packagesFor(category: string, basePrice: number): ServicePackage[] {
  return [
    {
      id: 'p1',
      tier: 'base',
      title: 'Base',
      description: `Intervento ${category.toLowerCase()} essenziale`,
      price: basePrice,
      duration: '1-2 ore',
    },
    {
      id: 'p2',
      tier: 'standard',
      title: 'Standard',
      description: `Servizio ${category.toLowerCase()} completo`,
      price: Math.round(basePrice * 1.8),
      duration: '2-4 ore',
    },
    {
      id: 'p3',
      tier: 'premium',
      title: 'Premium',
      description: `Pacchetto ${category.toLowerCase()} premium con garanzia estesa`,
      price: Math.round(basePrice * 2.6),
      duration: '4-6 ore',
    },
  ];
}

function buildProfessional(seed: ZoneProfessionalSeed): Professional {
  const images = PROFESSIONAL_IMAGES[seed.imageKey];
  return {
    id: seed.id,
    name: seed.name,
    categorySlug: seed.categorySlug,
    category: seed.category,
    imageUrl: images.avatar,
    heroImageUrl: images.hero,
    avatarColor: seed.avatarColor,
    rating: seed.rating,
    reviewCount: seed.reviewCount,
    jobsCompleted: Math.round(seed.reviewCount * 2.4),
    pricePerHour: seed.priceFrom,
    distanceKm: seed.distanceKm,
    availableToday: seed.availableToday,
    verified: seed.verified ?? true,
    badges: { document: true, phone: true, professional: true },
    bio: DEFAULT_BIO,
    whyChoose: DEFAULT_WHY,
    packages: packagesFor(seed.category, seed.priceFrom),
    city: seed.city,
    serviceAreas: seed.serviceAreas,
  };
}

